package org.nju.demo.pojo.vo;

public class AstVO {

    private String treeSnippet;

    private String vectorSnippet;

    private int state;

    public String getTreeSnippet() {
        return treeSnippet;
    }

    public void setTreeSnippet(String treeSnippet) {
        this.treeSnippet = treeSnippet;
    }

    public String getVectorSnippet() {
        return vectorSnippet;
    }

    public void setVectorSnippet(String vectorSnippet) {
        this.vectorSnippet = vectorSnippet;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }
}
